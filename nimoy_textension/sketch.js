// This is a p5.js recovery of the handcoded monoline font from
// "Textension: Word Processor Variations", a Windows 95/98/XP program
// developed by JT Nimoy in 1999 at the Aesthetics & Computation Group
// of the MIT Media Laboratory. A screen recording of Textension 
// can be found at https://player.vimeo.com/video/6121230.
// Additional documentation may be found at: 
// https://jtnimoy.cc/item.php%3Fhandle=14882287-textension-word-processor-variations.html
//
// Nimoy wrote: 
// "Textension was a collection of 10 interactive experiments in making 
// creative variations of word processing applications. It was my response 
// as an artist to the way programmers always use the typewriter metaphor 
// when they are creating a typesetting application. Textension combines 
// the metaphor of the typewriter with that of other things in the 
// physical world, such as the act of blowing soap bubbles."
//
// Textension font data was extracted by Kyle McDonald by decompiling the
// Textension.exe at https://acid-play.com/download/textension with Hex-Rays.
// p5.js display program by Golan Levin, May 2025.


const DEFAULT_SCALE = 30;
let textensionDataInt8 = [];
let glyphData = {};


function setup() {
  createCanvas(800, 400);
  loadTextensionData();
}


function draw() {
  background("black");
  stroke("white");
  drawString("ABCDEFGHIJKLMNOPQRSTUVWXYZ", 100, 100, 30, 1);
  drawString("abcdefghijklmnopqrstuvwxyz", 100, 150, 30, 1);
  drawString("!@#$%^&*~-=+;:,.?'\" ()[]{}/|\\", 100, 200, 30, 1);
  drawString("0123456789 - Hello World", 100, 250, 30, 1);
  
  drawString(
    'From "Textension: Word Processor Variations" (1999)',
    100, 300, 12, 1);
  drawString(
    "by JT Nimoy - https://player.vimeo.com/video/6121230",
    100, 325, 12, 1);
}


function drawGlyph(whichGlyph, x, y, glyphScale = 20, lineTh = 1) {
  if (!glyphData[whichGlyph]) {
    console.warn("Symbol not found:", whichGlyph);
    return;
  }
  let glyphObj = glyphData[whichGlyph];
  let paths = glyphObj.paths;

  push();
  translate(x, y);
  scale(glyphScale, glyphScale);
  strokeWeight(lineTh / glyphScale);
  noFill();
  for (let path of paths) {
    beginShape();
    for (let pt of path) {
      vertex(pt[0], pt[1]);
    }
    endShape();
  }
  pop();
}


function drawString(txt, startX, startY, sc = DEFAULT_SCALE, th = 1) {
  let x = startX;
  let y = startY;

  for (let c of txt) {
    if (!glyphData[c]) {
      x += sc;
      continue;
    }

    let glyphObj = glyphData[c];
    drawGlyph(c, x, y, sc, th);

    let glyphWidth = glyphObj.w !== undefined ? glyphObj.w : 1;
    x += glyphWidth * 1.55 * sc;
  }
}


function loadTextensionData() {
  for (let j = 0; j < decompiledTextensionData.length; j++) {
    let aChar = decompiledTextensionData[j];
    let code = aChar.charCodeAt(0);
    let int8 = code > 127 ? code - 256 : code;
    textensionDataInt8[j] = int8;
  }

  let i = 0;
  while (i < textensionDataInt8.length) {
    let j = i;
    let aChar = decompiledTextensionData[j];
    let code = aChar.charCodeAt(0);
    let coords = [];
    while (code != 13 && j < textensionDataInt8.length) {
      code = decompiledTextensionData[j].charCodeAt(0);
      if (j > i && code != 13) {
        coords.push(textensionDataInt8[j]);
      }
      j++;
    }
    if (coords.length % 4 === 0) {
      let raw = [];
      for (let k = 0; k + 3 < coords.length; k += 4) {
        let x1 = coords[k];
        let y1 = coords[k + 1];
        let x2 = coords[k + 2];
        let y2 = coords[k + 3];
        raw.push([x1, y1, x2, y2]);
      }
      glyphData[aChar] = { raw: raw };
    }
    i = j;
  }

  for (let whichGlyph in glyphData) {
    let glyphObj = glyphData[whichGlyph];
    let paths = parseRawGlyph(glyphObj);
    glyphObj.paths = paths;
  }
}


function parseRawGlyph(glyphObj) {
  let paths = [];
  for (let seg of glyphObj.raw) {
    let [x1, y1, x2, y2] = seg;
    let p1 = [x1 * 0.01, -y1 * 0.01];
    let p2 = [x2 * 0.01, -y2 * 0.01];
    paths.push([p1, p2]);
  }
  let xmax = Math.max(...paths.flat().map((p) => p[0]));
  glyphObj.w = xmax;
  return paths;
}


