(defn draw-curves
  [curves x-offset y-offset scale]
  (let [char-width scale
        char-height (* char-width 2.0)]
    (doseq [curve curves]
      (quil/begin-shape)
      (doseq [[x y] curve]
        (quil/vertex (+ x-offset (* char-width x))
                     (+ y-offset (* char-height y))))
      (quil/end-shape))))

(defn enumerate
  [s]
  (map-indexed vector s))

(defn write-something
  []
  (quil/background 255 255 255)
  (quil/stroke 0 0 0)
  (quil/stroke-weight 10)
  (quil/no-fill)

  (let [scale (* quil/width 0.02)
        spacing (* quil/width 0.03)]
    (doseq [[index c] (enumerate "HELLO WORLD")]
      (let [x-offset (+ (* quil/width 0.05) (* index spacing))
            y-offset (* quil/width 0.05)
            curves (get char-layouts (str c) char-placeholder)]
        (draw-curves curves x-offset y-offset scale)))))
