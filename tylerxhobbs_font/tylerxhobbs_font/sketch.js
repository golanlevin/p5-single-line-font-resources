// -----------------------------
function setup() {
  createCanvas(900, 330);
}

function draw() {
  background('black');
  stroke('white');
  strokeWeight(1);
  strokeJoin(ROUND);
  strokeCap(ROUND);
  noFill();

  drawString("ABCDEFGHIJKLMNOPQRSTUVWXYZ", 60, 60, 20);
  drawString(".,;:\'\"<>[]{}()/|\\?`~!@#$%^&*-_+=", 60,140, 14);
  drawString("0123456789 - HELLO WORLD", 60,190, 14);
  drawString("FONT BY TYLER X. HOBBS", 60,240, 14);
}

function keyPressed(){
  if (key == 's'){
    save("tylerxhobbs_font.png"); 
  }
}

